print(2 + 3)   # addition(+) -> Gives sum value
print(3 - 1)   # subtraction(-) -> Gives Difference
print(2 * 3)   # multiplication(*) -> Gives Product value
print(3 / 2)   # division(/) -> Gives Quotient
print(3 ** 2)  # exponential(**) -> Give the exponential answer
print(3 % 2)   # modulus(%) -> Gives the remainder i.e finding the remainder
print(3 // 2)  # Floor division operator(//) -> Gives the remainder i.e removing the remainder

# Checking data types

print(type(10))                  # Int
print(type(3.14))                # Float
print(type(1 + 3j))              # Complex
print(type('Asabeneh'))          # String
print(type([1, 2, 3]))           # List
print(type({'name':'Asabeneh'})) # Dictionary
print(type({9.8, 3.14, 2.7}))    # Set
print(type((9.8, 3.14, 2.7)))    # Tuple

